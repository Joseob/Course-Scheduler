    private void DropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentSubmitButtonActionPerformed
        // TODO add your handling code here:
        String name = DropStudentComboBox.getSelectedItem().toString();
        String studentID = name.split(" ")[1];
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        StudentQueries.dropStudent(studentID);
        DropStudentTextArea.append("\n" + name + " has been dropped from the list of students.\n");
        for (String semester:semesters)
        {
            if(ScheduleQueries.getScheduleByStudent(semester, studentID).size() != 0)
            {
                DropStudentTextArea.append("\nFor Semester: " + semester + "\n");
                ArrayList<ScheduleEntry> scheduleInfo = ScheduleQueries.getScheduleByStudent(semester, studentID);
                for (ScheduleEntry info:scheduleInfo)
                {
                    if (info.getStatus().equals("S"))
                    {
                        ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, info.getCourseCode());
                        DropStudentTextArea.append(name + " has been dropped from " + info.getCourseCode() + "\n");
                        if (ScheduleQueries.getWaitlistedStudentsByClass(semester, info.getCourseCode()).size() != 0)
                        {
                            ScheduleEntry firstStudent = ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, info.getCourseCode()).get(0);
                            ScheduleQueries.updateScheduleEntry(firstStudent);
                            StudentEntry person = StudentQueries.getStudent(firstStudent.getStudentId());
                            DropStudentTextArea.append(person.getLastName() + "," + person.getFirstName() + " " + firstStudent.getStudentId() + " has been scheduled into " + info.getCourseCode() + "\n");
                        }
                    }else{
                        ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, info.getCourseCode());
                        DropStudentTextArea.append(name + " " + "has been dropped from the waitlist for " + info.getCourseCode() + "\n");
                    }
                }
            }
        }
        rebuildDropClassChooseStudentComboBox();
        rebuildStudentNameComboBoxes();
        rebuilddisplayScheduleStudentComboBox();
        rebuildDropStudentComboBox();        
    }//GEN-LAST:event_DropStudentSubmitButtonActionPerformed

    private void DropClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropClassSubmitButtonActionPerformed
        // TODO add your handling code here:
        String selectedCourse = DropClassComboBox.getSelectedItem().toString();
        DropClassTextArea.append("\nScheduled students dropped from the course:\n");
        ArrayList<StudentEntry> scheduledStudents = MultiTableQueries.getScheduledStudentsByClass(currentSemester, selectedCourse);
        for (StudentEntry student:scheduledStudents){ 
            String studentID = student.getStudentID();
            StudentEntry person = StudentQueries.getStudent(studentID);
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, studentID, selectedCourse);
            DropClassTextArea.append(person.getLastName() + "," + person.getFirstName() + " " + studentID + '\n');
        }
        DropClassTextArea.append("\nWaitlisted students dropped from the course:\n");
        ArrayList<StudentEntry> waitlistedStudents = MultiTableQueries.getWaitlistedStudentsByClass(currentSemester, selectedCourse);
        for (StudentEntry student:waitlistedStudents){ 
            String studentID = student.getStudentID();
            StudentEntry person = StudentQueries.getStudent(studentID);
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, studentID, selectedCourse);
            DropClassTextArea.append(person.getLastName() + "," + person.getFirstName() + " " + studentID + '\n');
        }
        ClassQueries.dropClass(currentSemester, selectedCourse);
        rebuildClassComboBoxes();
        rebuildDropBoxChooseClassComboBox();
        rebuildStudentClassComboBoxes();
        rebuildDisplayChooseClassComboBox();
        rebuildDropClassComboBox();  
    }//GEN-LAST:event_DropClassSubmitButtonActionPerformed

