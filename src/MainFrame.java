    private void addSubmitScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSubmitScheduleButtonActionPerformed
        // TODO add your handling code here:
        String semester = currentSemester;
        String courseCode = selectClassComboBox.getSelectedItem().toString();
        String status;
        String studentID = ((StudentEntry) selectStudentComboBox.getSelectedItem()).getStudentID();
        int classSeats = ClassQueries.getClassSeats(semester, courseCode);
        int scheduled = ScheduleQueries.getScheduledStudentCount(semester, courseCode);
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        String text;

        if (classSeats > scheduled) {
            status = "S";
            text = "scheduled";
        } else {
            status = "W";
            text = "waitlisted";
        }
        ScheduleEntry entry = new ScheduleEntry(semester, courseCode, studentID, status, currentTimestamp);
        ScheduleQueries.addScheduleEntry(entry);
        scheduleClassStatusLabel.setText((StudentEntry) selectStudentComboBox.getSelectedItem() + " has been " + text + " for the class");                                       
    }//GEN-LAST:event_addSubmitScheduleButtonActionPerformed

